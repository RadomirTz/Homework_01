str_ = input('STR: ')


if re.fullmatch(ipv4, str_) != None:
    print('Вы ввели ipv4 адрес')

if re.fullmatch(ipv6, str_) != None:
    print('Вы ввели ipv6 адрес')

if re.fullmatch(mac, str_) != None:
    print('Вы ввели MAC-адрес')
    
if re.fullmatch(date, str_) != None:
    print('Вы ввели точную дату')